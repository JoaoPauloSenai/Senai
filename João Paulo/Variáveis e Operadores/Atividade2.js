//2) Faça um programa no qual o usuário digite dois números e mostre na tela a multiplicação desses números.
let numeroum, numerodois, multidosnumeros;

    numeroum =  Number(prompt ("Digite o 1° numero: "))
    numerodois = Number(prompt ("Digite 2° numero: "))

    multidosnumeros = numeroum * numerodois

    alert (`A multiplicação dos numeros é ${multidosnumeros}.`)